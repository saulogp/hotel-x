    private void btnAtuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtuActionPerformed
        // TODO add your handling code here:

        if (tabQuarto.getSelectedRow() != -1) {
            Quarto qua = new Quarto();
            QuartoDAO dao = new QuartoDAO();

            qua.setTipo(cbTipo.getSelectedItem().toString());
            qua.setDescricao(txtDes.getText());
            qua.setNumero(Integer.parseInt(txtNum.getText()));
            qua.setAtivo(cbAtivo.getSelectedItem().toString());
            qua.setValor((int) Double.parseDouble(txtValor.getText()));
            qua.setQtd_pessoas(Integer.parseInt(txtQtdPessoas.getText()));
            qua.setNum_cama(Integer.parseInt(txtNumCamas.getText()));
            qua.setId((int)tabQuarto.getValueAt(tabQuarto.getSelectedRow(), 0));

            dao.update(qua);

            cbTipo.setSelectedIndex(0);
            txtDes.setText("");
            txtNum.setText("");
            cbAtivo.setSelectedIndex(0);
            txtValor.setText("");
            txtQtdPessoas.setText("");
            txtNumCamas.setText("");

            readTable();
        }
    }//GEN-LAST:event_btnAtuActionPerformed

