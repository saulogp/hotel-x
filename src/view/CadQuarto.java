    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        Quarto quarto = new Quarto();
        QuartoDAO dao = new QuartoDAO();

        quarto.setTipo((String) cbTipo.getSelectedItem());
        quarto.setDescricao(txtDes.getText());
        quarto.setNumero(Integer.parseInt(txtNum.getText()));
        quarto.setValor(Double.parseDouble(txtValor.getText()));
        quarto.setNum_cama(Integer.parseInt(txtNumCamas.getText()));
        quarto.setQtd_pessoas(Integer.parseInt(txtQtdPessoas.getText()));
        quarto.setAtivo(cbAtivo.getSelectedItem().toString());

        dao.create(quarto);

        cbTipo.setSelectedIndex(0);
        txtDes.setText("");
        txtNum.setText("");
        txtValor.setText("");
        txtNumCamas.setText("");
        txtQtdPessoas.setText("");
        cbAtivo.setSelectedIndex(0);

        readTable();
    }//GEN-LAST:event_btnAddActionPerformed

