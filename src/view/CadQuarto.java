    private void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabQuarto.getModel();
        QuartoDAO qdao = new QuartoDAO();
        modelo.setNumRows(0);
        for (Quarto q : qdao.read()) {
            modelo.addRow(new Object[]{
                q.getId(),
                q.getTipo(),
                q.getNumero(),
                q.getValor(),
                q.getAtivo(),
                q.getQtd_pessoas(),
                q.getNum_cama(),
                q.getDescricao()
            });
        }
    }
    
    private void readTableForType(String tipo) {
        DefaultTableModel modelo = (DefaultTableModel) tabQuarto.getModel();
        QuartoDAO qdao = new QuartoDAO();
        modelo.setNumRows(0);
        for (Quarto q : qdao.readForType(tipo)) {
            modelo.addRow(new Object[]{
                q.getId(),
                q.getTipo(),
                q.getNumero(),
                q.getValor(),
                q.getAtivo(),
                q.getQtd_pessoas(),
                q.getNum_cama(),
                q.getDescricao()
            });
        }
    }

