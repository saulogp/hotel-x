    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (tabCliente.getSelectedRow() != -1) {
            Cliente cli = new Cliente();
            ClienteDAO dao = new ClienteDAO();
            
            cli.setNome(txtNome.getText());
            cli.setSobrenome(txtSobrenome.getText());
            cli.setCpf(txtCpf.getText());
            cli.setRg(txtRg.getText());
            cli.setSexo(cbSexo.getSelectedItem().toString());
            cli.setEndereco(txtEnde.getText());
            cli.setNumCasa(Integer.parseInt(txtNumCasa.getText()));
            cli.setCep(txtCep.getText());
            cli.setCidade(txtCidade.getText());
            cli.setUf(cbUf.getSelectedItem().toString());
            cli.setPais(txtPais.getText());
            cli.setTelefone(txtTelefone.getText());
            cli.setCelular(txtCelular.getText());
            cli.setEmail(txtEmail.getText());
            cli.setId((int)tabCliente.getValueAt(tabCliente.getSelectedRow(), 0));

            dao.update(cli);

            txtNome.setText("");
            txtSobrenome.setText("");
            txtCpf.setText("");
            txtRg.setText("");
            cbSexo.setSelectedIndex(0);
            txtEnde.setText("");
            txtNumCasa.setText("");
            txtCep.setText("");
            txtCidade.setText("");
            cbUf.setSelectedIndex(0);
            txtPais.setText("");
            txtTelefone.setText("");
            txtCelular.setText("");
            txtEmail.setText("");

            readTable();
        }  


    }//GEN-LAST:event_btnUpdateActionPerformed

