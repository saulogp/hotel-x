    private void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabCliente.getModel();
        ClienteDAO cdao = new ClienteDAO();
        modelo.setNumRows(0);
        for (Cliente c : cdao.read()) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getSobrenome(),
                c.getCpf(),
                c.getRg(),
                c.getSexo(),
                c.getEndereco(),
                c.getNumCasa(),
                c.getCep(),
                c.getCidade(),
                c.getUf(),
                c.getPais(),
                c.getTelefone(),
                c.getCelular(),
                c.getEmail()
            });
        }
    }
    
    private void readTableForName(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) tabCliente.getModel();
        ClienteDAO cdao = new ClienteDAO();
        modelo.setNumRows(0);
        for (Cliente c : cdao.readForName(nome)) {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNome(),
                c.getSobrenome(),
                c.getCpf(),
                c.getRg(),
                c.getSexo(),
                c.getEndereco(),
                c.getNumCasa(),
                c.getCep(),
                c.getCidade(),
                c.getUf(),
                c.getPais(),
                c.getTelefone(),
                c.getCelular(),
                c.getEmail()
            });
        }
    }

