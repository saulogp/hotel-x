    private void btnAtuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtuActionPerformed
        // TODO add your handling code here:

        if (tabUsuario.getSelectedRow() != -1) {
            Usuario user = new Usuario();
            UsuarioDAO dao = new UsuarioDAO();

            user.setNome(txtNome.getText());
            user.setSobrenome(txtSobrenome.getText());
            user.setUsuario(txtUsu.getText());
            user.setSenha(txtSenha.getText());
            user.setAtivo(cbAtivo.getSelectedItem().toString());
            user.setId((int) tabUsuario.getValueAt(tabUsuario.getSelectedRow(), 0));
            if (!"".equals(txtSenha.getText())) {
                dao.update(user);
                txtNome.setText("");
                txtSobrenome.setText("");
                txtUsu.setText("");
                txtSenha.setText("");
                txtConfirma.setText("");
                cbAtivo.setSelectedIndex(0);
            } else{
                JOptionPane.showMessageDialog(null, "Informe a senha para atualizar!");
            }

            readTable();
        }


    }//GEN-LAST:event_btnAtuActionPerformed

