    private void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabUsuario.getModel();
        UsuarioDAO udao = new UsuarioDAO();
        modelo.setNumRows(0);
        for (Usuario u : udao.read()) {
            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getSobrenome(),
                u.getUsuario(),
                u.getAtivo()
            });
        }
    }
    
    private void readTableForName(String nome) {
        DefaultTableModel modelo = (DefaultTableModel) tabUsuario.getModel();
        UsuarioDAO udao = new UsuarioDAO();
        modelo.setNumRows(0);
        for (Usuario u : udao.readForNome(nome)) {
            modelo.addRow(new Object[]{
                u.getId(),
                u.getNome(),
                u.getSobrenome(),
                u.getUsuario(),
                u.getAtivo()
            });
        }
    }

