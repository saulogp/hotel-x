    private void btnAltResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAltResActionPerformed
        // TODO add your handling code here:
        if (tabReserva.getSelectedRow() != -1) {
            Reserva res = new Reserva();
            ReservaDAO dao = new ReservaDAO();

            res.setCodQuarto(Integer.parseInt(txtQ.getText()));
            res.setCodUsuario(Integer.parseInt(txtU.getText()));
            res.setCodCliente(Integer.parseInt(txtC.getText()));
            res.setCheckIn(txtCheckIn.getText());
            res.setCheckOut(txtCheckOut.getText());
            res.setQtdAdulto(Integer.parseInt(txtA.getText()));
            res.setQtdCriancas(Integer.parseInt(txtCri.getText()));
            res.setId((int) tabReserva.getValueAt(tabReserva.getSelectedRow(), 0));

            dao.update(res);

            txtQ.setText("");
            txtU.setText("");
            txtC.setText("");
            txtCheckIn.setText("");
            txtCheckOut.setText("");
            txtA.setText("");
            txtCri.setText("");

            readTable();
        }
    }//GEN-LAST:event_btnAltResActionPerformed

