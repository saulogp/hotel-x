    private void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) tabReserva.getModel();
        ReservaDAO rdao = new ReservaDAO();
        modelo.setNumRows(0);
        for (Reserva q : rdao.read()) {
            modelo.addRow(new Object[]{
                q.getId(),
                q.getCodQuarto(),
                q.getCodUsuario(),
                q.getCodCliente(),
                q.getCheckIn(),
                q.getCheckOut(),
                q.getQtdAdulto(),
                q.getQtdCriancas(),
                q.getComentarios()
            });
        }
    }

    private void readTableForData(String check_in) {
        DefaultTableModel modelo = (DefaultTableModel) tabReserva.getModel();
        ReservaDAO rdao = new ReservaDAO();
        modelo.setNumRows(0);
        for (Reserva q : rdao.readForData(check_in)) {
            modelo.addRow(new Object[]{
                q.getId(),
                q.getCodQuarto(),
                q.getCodUsuario(),
                q.getCodCliente(),
                q.getCheckIn(),
                q.getCheckOut(),
                q.getQtdAdulto(),
                q.getQtdCriancas(),
                q.getComentarios()
            });
        }
    }

