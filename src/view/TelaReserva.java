    private void btnConResActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConResActionPerformed

        Reserva reserva = new Reserva();
        ReservaDAO dao = new ReservaDAO();

        if (dao.checkReserva(txtCheckIn.getText(), Integer.parseInt(txtQ.getText()))) {

            reserva.setCodQuarto(Integer.parseInt(txtQ.getText()));
            reserva.setCodUsuario(Integer.parseInt(txtU.getText()));
            reserva.setCodCliente(Integer.parseInt(txtC.getText()));
            reserva.setCheckIn(txtCheckIn.getText());
            reserva.setCheckOut(txtCheckOut.getText());
            reserva.setQtdAdulto(Integer.parseInt(txtA.getText()));
            reserva.setQtdCriancas(Integer.parseInt(txtCri.getText()));
            reserva.setComentarios(txtCom.getText());

            dao.create(reserva);

            txtQ.setText("");
            txtU.setText("");
            txtC.setText("");
            txtCheckIn.setText("");
            txtCheckOut.setText("");
            txtA.setText("");
            txtCri.setText("");
            txtCom.setText("");

            readTable();

        } else {
            JOptionPane.showMessageDialog(null, "Quarto e data já reservados!");
            JOptionPane.showMessageDialog(null, "Escolha outra data ou quarto!");
        }


    }//GEN-LAST:event_btnConResActionPerformed

